#!/usr/bin/env bash

set -e

SSO_START_URL="https://swisstopo.awsapps.com/start#"
SSO_REGION="eu-central-1"
DEFAULT_REGION="eu-central-1"
OUTPUT_FILE="${1:-$HOME/.aws/config}"

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

# Find a valid SSO access token from the cache
get_access_token() {
    local now
    now=$(date -u +%s)
    local token=""

    for cache_file in "$HOME"/.aws/sso/cache/*.json; do
        [[ -f "$cache_file" ]] || continue
        local start_url expires_at access_token
        start_url=$(python3 -c "import json,sys; d=json.load(open('$cache_file')); print(d.get('startUrl',''))" 2>/dev/null)
        [[ "$start_url" == "$SSO_START_URL" ]] || continue
        expires_at=$(python3 -c "import json,sys; d=json.load(open('$cache_file')); print(d.get('expiresAt',''))" 2>/dev/null)
        access_token=$(python3 -c "import json,sys; d=json.load(open('$cache_file')); print(d.get('accessToken',''))" 2>/dev/null)
        [[ -z "$access_token" ]] && continue
        local expires_epoch
        expires_epoch=$(date -u -d "$expires_at" +%s 2>/dev/null || date -u -j -f "%Y-%m-%dT%H:%M:%SZ" "$expires_at" +%s 2>/dev/null || echo 0)
        if [[ "$expires_epoch" -gt "$now" ]]; then
            token="$access_token"
            break
        fi
    done

    echo "$token"
}

# Ensure we have a valid SSO session
ACCESS_TOKEN=$(get_access_token)
if [[ -z "$ACCESS_TOKEN" ]]; then
    echo -e "${YELLOW}No valid SSO session found, logging in...${NC}"
    aws sso login --start-url "$SSO_START_URL" --sso-region "$SSO_REGION"
    ACCESS_TOKEN=$(get_access_token)
    if [[ -z "$ACCESS_TOKEN" ]]; then
        echo -e "${RED}Failed to obtain SSO access token.${NC}"
        exit 1
    fi
fi

echo -e "${YELLOW}Discovering accounts and roles...${NC}"

# Collect all accounts
accounts=$(aws sso list-accounts \
    --access-token "$ACCESS_TOKEN" \
    --region "$SSO_REGION" \
    --output json \
    --query 'accountList[*].{id:accountId,name:accountName}')

account_count=$(echo "$accounts" | python3 -c "import json,sys; print(len(json.load(sys.stdin)))")
echo -e "Found ${account_count} accounts."

# Build new config content (avoid $() heredoc — command substitution strips trailing newlines)
config="# Generated by swisstopo-aws.sh — do not edit manually
# Use of aws-sso sessions break terraform use cases
# [sso-session bgdi]
# sso_start_url = https://swisstopo.awsapps.com/start#
# sso_region = eu-central-1
# sso_registration_scopes = sso:account:access

"

while IFS= read -r account_id; read -r account_name; do
    roles=$(aws sso list-account-roles \
        --access-token "$ACCESS_TOKEN" \
        --account-id "$account_id" \
        --region "$SSO_REGION" \
        --output json \
        --query 'roleList[*].roleName' 2>/dev/null || echo "[]")

    while IFS= read -r role_name; do
        [[ -z "$role_name" ]] && continue

        # Derive a profile name: lowercase account name + role suffix if multiple roles
        role_count=$(echo "$roles" | python3 -c "import json,sys; print(len(json.load(sys.stdin)))")
        profile_base=$(echo "$account_name" | tr '[:upper:]' '[:lower:]' | tr ' _' '--' | tr -cd '[:alnum:]-')
        if [[ "$role_count" -gt 1 ]]; then
            role_slug=$(echo "$role_name" | tr '[:upper:]' '[:lower:]' | tr ' _' '--' | tr -cd '[:alnum:]-')
            profile_name="${profile_base}-${role_slug}"
        else
            profile_name="$profile_base"
        fi

        region="$DEFAULT_REGION"

        config+="[profile ${profile_name}]
region = ${region}
output = yaml
cli_pager =
sso_account_id = ${account_id}
sso_role_name = ${role_name}
sso_start_url = ${SSO_START_URL}
sso_region = ${SSO_REGION}

"
        echo -e "  ${GREEN}+${NC} [${profile_name}]  (${account_id} / ${role_name})"
    done < <(echo "$roles" | python3 -c "import json,sys; [print(r) for r in json.load(sys.stdin)]")

done < <(echo "$accounts" | python3 -c "
import json, sys
for a in json.load(sys.stdin):
    print(a['id'])
    print(a['name'])
")

# Write output
mkdir -p "$(dirname "$OUTPUT_FILE")"
printf '%s' "$config" > "$OUTPUT_FILE"
echo -e "\n${GREEN}Written to ${OUTPUT_FILE}${NC}"
